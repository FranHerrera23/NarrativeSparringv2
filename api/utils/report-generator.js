/**
 * Basic markdown to HTML converter
 * (Simple implementation for fallback)
 */
function convertMarkdownToHTML(markdown) {
  let html = markdown;

  // Headers
  html = html.replace(/^### (.*$)/gim, '<h3>$1</h3>');
  html = html.replace(/^## (.*$)/gim, '<h2>$1</h2>');
  html = html.replace(/^# (.*$)/gim, '<h1>$1</h1>');

  // Bold
  html = html.replace(/\*\*(.*?)\*\*/gms, '<strong>$1</strong>');

  // Italic
  html = html.replace(/\*([^*\n]+)\*/g, '<em>$1</em>');

  // Links
  html = html.replace(/\[(.*?)\]\((.*?)\)/g, '<a href="$2">$1</a>');

  // Paragraphs
  html = html.replace(/\n\n/g, '</p><p>');
  html = '<p>' + html + '</p>';

  // Clean up empty paragraphs
  html = html.replace(/<p><\/p>/g, '');
  html = html.replace(/<p>\s*<\/p>/g, '');

  return html;
}

/**
 * Generate PDF using PDFKit (serverless-compatible)
 * @param {string} markdown - The markdown content from Claude
 * @param {Object} options - Generation options
 * @returns {Promise<Buffer>} - PDF buffer
 */
async function generatePDFKit(markdown, options = {}) {
  const {
    title = 'Narrative Sparring Diagnostic Report',
    author = 'CRUDA',
  } = options;

  return new Promise((resolve, reject) => {
    try {
      const doc = new PDFDocument({
        size: 'LETTER',
        margins: { top: 50, bottom: 50, left: 50, right: 50 },
        info: {
          Title: title,
          Author: author,
        },
      });

      const chunks = [];
      doc.on('data', (chunk) => chunks.push(chunk));
      doc.on('end', () => resolve(Buffer.concat(chunks)));
      doc.on('error', (error) => reject(error));

      // Add CRUDA branding header
      doc
        .fontSize(24)
        .font('Helvetica-Bold')
        .text('CRUDA', { align: 'center' });

      doc
        .fontSize(14)
        .font('Helvetica')
        .text('Narrative Sparring', { align: 'center' });

      doc.moveDown(0.5);
      doc
        .moveTo(50, doc.y)
        .lineTo(562, doc.y)
        .stroke();
      doc.moveDown(1);

      // Add title
      doc
        .fontSize(18)
        .font('Helvetica-Bold')
        .text(title, { align: 'center' });

      doc.moveDown(2);

      // Parse and add markdown content
      const lines = markdown.split('\n');
      let currentFontSize = 11;
      let currentFont = 'Helvetica';

      for (let i = 0; i < lines.length; i++) {
        let line = lines[i];

        // Skip empty lines
        if (!line.trim()) {
          doc.moveDown(0.5);
          continue;
        }

        // Headers
        if (line.startsWith('# ')) {
          doc.fontSize(20).font('Helvetica-Bold').text(line.substring(2));
          doc.moveDown(0.5);
          continue;
        }

        if (line.startsWith('## ')) {
          doc.fontSize(16).font('Helvetica-Bold').text(line.substring(3));
          doc.moveDown(0.4);
          continue;
        }

        if (line.startsWith('### ')) {
          doc.fontSize(14).font('Helvetica-Bold').text(line.substring(4));
          doc.moveDown(0.3);
          continue;
        }

        // Bold text (simple approach - just detect ** markers)
        if (line.includes('**')) {
          line = line.replace(/\*\*/g, '');
          doc.fontSize(11).font('Helvetica-Bold').text(line);
          doc.moveDown(0.3);
          continue;
        }

        // Regular text
        doc.fontSize(11).font('Helvetica').text(line, {
          align: 'left',
          lineGap: 2,
        });
        doc.moveDown(0.2);
      }

      // Add footer
      doc.moveDown(2);
      doc
        .moveTo(50, doc.y)
        .lineTo(562, doc.y)
        .stroke();
      doc.moveDown(0.5);

      doc
        .fontSize(10)
        .font('Helvetica')
        .text('This report was generated by CRUDA\'s narrative diagnostic engine.', {
          align: 'center',
        });

      doc
        .fontSize(10)
        .text('Questions? Contact us at support@cruda.io', {
          align: 'center',
        });

      doc.end();

    } catch (error) {
      reject(new Error(`PDF generation failed: ${error.message}`));
    }
  });
}

